// Замени выражения со стандартными математеческими операторами на
// комбинированный оператор присвоения с добавлением, вычитанием, умножением и делением.

let a = 5;
let b = 10;
let c = 15;
let d = 20;

a += 2;
b -= 4;
c *= 3;
d /= 10;

console.log('a: ', a); // 7
console.log('b: ', b); // 6
console.log('c: ', c); // 45
console.log('d: ', d); // 2
